    /**
     * Boton Registar
     * 
     * 
     * Validamos los campos de texto, guardamos el texto ingresado en los campos.
     * 
     * 
     * @param evt 
     **/
    
            
    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed

        if (txtCedula.getText().isEmpty()|| txtNombre.getText().isEmpty()|| txtApellido.getText().isEmpty()|| txtCorreo.getText().isEmpty()
                || txtContrasena.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, alerta01);

        } else {

            controladorUsuario.registrarUsuario(txtCedula.getText(), txtNombre.getText(),
                    txtApellido.getText(), txtCorreo.getText(), txtContrasena.getText());

            JOptionPane.showMessageDialog(this, alerta02);

            limpiar();
            this.dispose();

        }

    }//GEN-LAST:event_btnRegistrarseActionPerformed

